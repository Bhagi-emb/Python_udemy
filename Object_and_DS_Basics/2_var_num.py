#Basic Variable assignment
mydogs = 2
mydogs = ['tommy', 'jerry']
print('\n')
print('type of varible:  ',type(mydogs))            #type of varible:   <class 'list'>
print('mydogs : ', mydogs)                          #mydogs :  ['tommy', 'jerry']

#type of variable
# # int
a = 2
print('type of varible:  ',type(a))                 #type of varible:   <class 'int'>
print('a : ', a)                                    #a :  2
a = 3
print('type of varible:  ',type(a))                 #type of varible:   <class 'int'>
print('a : ', a)                                    #a :  3

# # String
a = 'hello'
print('type of varible:  ',type(a))                 #type of varible:   <class 'str'>
print('a : ', a)                                    #a :  hello
# # String with double quotes
a = '''hello'''             
print('type of varible:  ',type(a))                 #type of varible:   <class 'str'>
print('a : ', a)                                    #a :  hello
# # String with single quotes
a = "hello"                                         
print('type of varible:  ',type(a))                 #type of varible:   <class 'str'>
print('a : ', a)                                    #a :  hello

#float
a = 2.0
print('type of varible:  ',type(a))                 #type of varible:   <class 'float'>
print('a : ', a)                                    #a :  2.0