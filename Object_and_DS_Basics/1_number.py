#Basic calculation in python
# Addition
print('addition : ',2+3)                            #addition :  5
# Subtraction
print('subtraction : ',2-3)                         #subtraction :  -1
# Multiplication
print('multiplication : ',2*3)                      #multiplication :  6
# Division
print('division : ',2/3)                            #division :  0.6666666666666666
# Floor Division - divides two numbers and rounds the result down to an integer
print('floor division : ',8//3)                     #floor division :  2
# Modulus
print('modulus : ',2%3)                             #modulus :  2
# Exponentiation  -- power
print('exponentiation : ',2**3)                     #exponentiation :  8
# Square root
print('square root : ',2**0.5)                      #square root :  1.4142135623730951
# Cube root
print('cube root : ',2**(1/3))                      #cube root :  1.2599210498948732                          
# Square -- power
print('square : ',2**2)                             #square :  4
# Absolute value
print('absolute value : ',abs(-3))                  #absolute value :  3
# Round
print('round : ',round(3.75))                       #round :  4
